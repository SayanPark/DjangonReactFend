function Category() {
  const { slug } = useParams(); // get the slug from the route
  const [selectedDepartment, setSelectedDepartment] = useState(null);
  const [posts, setPosts] = useState([]);
  const [, setCategoryImage] = useState(null);
  const [openDropdowns, setOpenDropdowns] = React.useState({});

  useEffect(() => {
    const entry = Object.entries(departmentsDictionary).find(
      ([, value]) => value.slug === slug
    );
    if (entry) {
      const [name, data] = entry;
      setSelectedDepartment({ name, ...data });
    }
  }, [slug]);

  useEffect(() => {
    if (slug) {
      apiInstance
        .get(`/post/category/posts/${slug}/`)
        .then((response) => setPosts(response.data))
        .catch((error) => console.error("Error fetching posts:", error));
    }
  }, [slug]);
  
  useEffect(() => {
    if (slug) {
      // Use the same image mapping logic as CategorySlider.jsx
      const categoryImage = getCategoryImage(slug);
      if (categoryImage) {
        setCategoryImage(categoryImage);
        setSelectedDepartment((prev) => ({
          ...prev,
          image: categoryImage,
        }));
      } else {
        // Fallback to API if no mapped image found
        apiInstance
          .get(`/post/category/list/`)
          .then((response) => {
            if (response.data && Array.isArray(response.data)) {
              const category = response.data.find((cat) => cat.slug === slug);
              if (category && category.image_url) {
                setCategoryImage(category.image_url);
                setSelectedDepartment((prev) => ({
                  ...prev,
                  image: category.image_url,
                }));
              }
            }
          })
          .catch((error) => console.error("Error fetching category list:", error));
      }
    }
  }, [slug]);

  // Recursive function to render nested dictionaries with dropdowns for keys starting with "• " except "• بر اساس "
  const renderNestedDictionary = (dict, parentKey = "") => {
    const toggleDropdown = (key) => {
      setOpenDropdowns((prev) => ({
        ...prev,
        [key]: !prev[key],
      }));
    };

    return (
      <>
        {Object.entries(dict).map(([key, value]) => {
          const combinedKey = parentKey ? `${parentKey}__${key}` : key;

          if (key.startsWith("description")) {
            return <p key={combinedKey}>{value}</p>;
          }
            if (Array.isArray(value)) {
              if (key.startsWith("• ") && !key.startsWith("• بر اساس ")) {
                return (
                  <div key={combinedKey} style={{ paddingRight: "1rem" }}>
                    <h4
                      style={{
                        fontSize: "1.2rem",
                        fontWeight: "bold",
                        cursor: "pointer",
                        color:
                          value.length === 0
                            ? "var(--bs-secondary)"
                            : "var(--bs-primary)",
                      }}
                      onClick={() => toggleDropdown(combinedKey)}
                    >
                      {key} {openDropdowns[combinedKey] ? "▲" : "▼"}
                    </h4>
                    {openDropdowns[combinedKey] &&
                      value.map((item, idx) => (
                        <p key={idx} style={{ paddingRight: "1rem" }}>
                          {item}
                        </p>
                      ))}
                  </div>
                );
              } else {
                return (
                  <div key={combinedKey} style={{ paddingRight: "1rem" }}>
                    <h4
                      style={{
                        fontSize: "1.2rem",
                        fontWeight: "bold",
                        color:
                          value.length === 0
                            ? "var(--bs-secondary)"
                            : "var(--bs-dark)",
                      }}
                    >
                      {key}
                    </h4>
                    {value.map((item, idx) => (
                      <p key={idx}>{item}</p>
                    ))}
                  </div>
                );
              }
            }
            if (typeof value === "object" && value !== null) {
              if (key.startsWith("• ") && !key.startsWith("• بر اساس ")) {
                return (
                  <div key={combinedKey} style={{ paddingRight: "1rem" }}>
                    <h4
                      style={{
                        fontSize: "1.2rem",
                        fontWeight: "bold",
                        cursor: "pointer",
                        color: key === "• تولیدکنندگان محصولات غذایی سالم" ? "var(--bs-primary)" : undefined,
                      }}
                      onClick={() => toggleDropdown(combinedKey)}
                    >
                      {key} {openDropdowns[combinedKey] ? "▲" : "▼"}
                    </h4>
                    {openDropdowns[combinedKey] && renderNestedDictionary(value, combinedKey)}
                  </div>
                );
              }
              return (
                <div key={combinedKey} style={{ paddingRight: "1rem" }}>
                  <h4 style={{ fontSize: "1.2rem", fontWeight: "bold" }}>{key}</h4>
                  {renderNestedDictionary(value, combinedKey)}
                </div>
              );
            }
          return null;
        })}
      </>
    );
  };

  return (
    <div dir="rtl">
      <section className="pt-0 pb-0 mt-0 mb-4">
        <div className="container">
          <div href="#" className="d-block card-img-flash mb-3">
            {selectedDepartment && selectedDepartment.image ? (
              <>
                <div className="category-image-container">
                  <img
                    src={selectedDepartment.image}
                    alt={selectedDepartment.name}
                    className="category-image-responsive"
                  />
                  <div className="category-image-overlay"></div>
                </div>
                <div className="row">
                  <div className="col-12 mb-3">
                    <h3 className=" mt-2">{selectedDepartment.name}</h3>
                    {renderNestedDictionary(selectedDepartment)}
                  </div>
                </div>
              </>
            ) : null}
          </div>
        </div>
      </section>

      <section className="bg-light pt-4 pb-4">
        <div className="container">
          <h2 className="mb-4">مقالات مرتبط</h2>
          <div className="row">
            {posts.length > 0 ? (
              posts.map((post) => (
                <div className="col-sm-6 col-lg-3" key={post?.id}>
                  <div className="card mb-4">
                    <div className="card-fold position-relative">
                      <img 
                        className="card-img" 
                        style={{ width: "100%", height: "160px", objectFit: "cover" }} 
                        src={post.image} 
                        alt="Card image" 
                        onError={(e) => {
                          e.target.style.display = 'none';
                          const shimmerContainer = document.createElement('div');
                          shimmerContainer.style.cssText = `
                            width: 100%;
                            height: 160px;
                            background-color: #f0f0f0;
                            border-radius: 4px;
                            position: relative;
                            overflow: hidden;
                          `;
                          shimmerContainer.innerHTML = `
                            <div style="
                              position: absolute;
                              top: 0;
                              left: 0;
                              width: 100%;
                              height: 100%;
                              background: linear-gradient(90deg, #f0f0f0 0%, #e0e0e0 50%, #f0f0f0 100%);
                              animation: shimmer 1.5s infinite;
                            "></div>
                            <style>
                              @keyframes shimmer {
                                0% { transform: translateX(-100%); }
                                100% { transform: translateX(100%); }
                              }
                            </style>
                          `;
                          e.target.parentNode.insertBefore(shimmerContainer, e.target.nextSibling);
                        }}
                      />
                  </div>
                  <div className="card-body px-3 pt-3">
                    <h4 className="card-title" style={{ overflow: "hidden", whiteSpace: "nowrap" }}>
                      <Link to={`/post/${post.slug}`} className="btn-link text-reset stretched-link fw-bold text-decoration-none post-title-text" title={post.title}>
                        {post.title.length > 18 ? post.title.substring(0, 18) + "…" : post.title}
                      </Link>
                    </h4>
                    <button style={{ border: "none", background: "none" }}>
                      <i className="fas fa-bookmark text-danger"></i>
                    </button>
                    <button style={{ border: "none", background: "none" }}>
                      <i className="fas fa-thumbs-up text-primary"></i>
                    </button>
                    <i className="fas fa-eye"></i> {post?.view}                                            
                    <ul className="mt-3 list-style-none" style={{ listStyle: "none", paddingRight: 0 }}>
                      <li>
                        <a href="#" className="text-dark text-decoration-none">
                          <i className="fas fa-user"></i> {post?.user?.full_name}
                        </a>
                      </li>
                      <li className="mt-2">
                        <i className="fas fa-calendar"></i> {Momment(post.date)}
                      </li>                                                
                    </ul>
                  </div>
                </div>
              </div>
              ))
            ) : (
              <p>هیچ مقاله‌ای یافت نشد.</p>
            )}
          </div>
        </div>
      </section>
    </div>
  );
}

export default Category;
